// Print the sum of series
// 1+2+3+4+5
public class _19_1_sum_of_series {
    public static void main(String[] args) {
        int i,sum=0;
        for(i=1;i<=5;i++){
            sum=sum+i;
        }
        System.out.println("The sum of series is: "+sum);
    }
    
}
